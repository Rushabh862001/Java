package corejava;

public class Explicit {

	public static void main(String[] args) {
		
		double d = 40.025;
		 
		//explicit type casting
		int i =(int)d;
		
		System.out.println("Double Data type value "+d);
		 
		//fractional part lost
		System.out.println("Int Data type value "+i);


	}

}